namespace Saber
{
    /// <summary>
    /// Узел двусвязного списка.
    /// </summary>
    public class ListNode
    {
        /// <summary>
        /// Предыдущий узел.
        /// </summary>
        public ListNode Previous;

        /// <summary>
        /// Следующий узел.
        /// </summary>
        public ListNode Next;

        /// <summary>
        /// Случайный узел.
        /// </summary>
        public ListNode Random;

        /// <summary>
        /// Данные.
        /// </summary>
        public string Data;
    }
}
